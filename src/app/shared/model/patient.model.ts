export class Patient {

  id:any
  firstName:any
  lastName:any
  photoUrl:any
  birthDate:any

  constructor(id:any,firstName:any,lastName:any,photoUrl:any,birthDate:any) {
    this.id = id;
    this.firstName = firstName;
    this.lastName = lastName;
    this.photoUrl = photoUrl;
    this.birthDate = birthDate;
  }
}
